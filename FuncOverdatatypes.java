public class FuncOverdatatypes {

    public static int sum(int a,int b){
        return a+b;
    }

    public static double sum(double a,Double b){
        return a+b;
    }
    public static void main(String args[]){
        System.out.println(sum(5, 03));
        System.out.println(sum(7.1, 0.9));
    }
}
